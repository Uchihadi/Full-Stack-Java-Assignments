package ArrayList;

// Collection Data Type to be mentioned with Diamond Operator <>

public class Product {
    String Name;
    double Price;

    public String getName(){
        return Name;
    }

    public void setName(String Name){
        this.Name = Name;
    }

    public double getPrice() {
        return Price;
    }

    public void setPrice(double Price){
        this.Price = Price;
    }
}
